package Flag;

public class FlagCard
{
    String fileName;
    double fileSize;

    public String getFileName()
    {
        return fileName;
    }

    public void setFileName(String fileName)
    {
        this.fileName = fileName;
    }

    public double getFileSize()
    {
        return fileSize;
    }

    public void setFileSize(double fileSize)
    {
        this.fileSize = fileSize;
    }
}
